function watcher(cb) {
  watch(coffeeSources).on('change', series(coffee))
  watch(jsSources).on('change', series(js))
  watch('components/sass/*.scss').on('change', series(compass))
  watch('builds/development/*.html').on('change', series(html))
  watch('builds/development/js/*.json').on('change', series(json))
  watch('builds/development/images/**/*.*').on('change', series(imagesReducer))
  cb();
}


/**gulp.task('connect', function() {
  connect.server({
    root: outputDir,
    livereload: true
  });
});**/
function connectServer(cb) {
  connect.server({
    root: outputDir,
    livereload: true
  });
  cb();
}

function html(cb) {
  src('builds/development/*.html')
    .pipe(gulpif(env === 'production', minifyHTML()))
    .pipe(gulpif(env === 'production', gulp.dest(outputDir)))
    .pipe(connect.reload());
  cb();
}

function imagesReducer(cb) {
  src('builds/development/images/**/*.*')
    .pipe(gulpif(env === 'production', imagemin({
      progressive: true,
      svgoPlugins: [{ removeViewBox: false }],
      use: [pngcrush()]
    })))
    .pipe(gulpif(env === 'production', gulp.dest(outputDir + 'images')))
    .pipe(connect.reload());
  cb();
}

function json(cb) {
  src('builds/development/js/*.json')
    .pipe(gulpif(env === 'production', jsonminify()))
    .pipe(gulpif(env === 'production', gulp.dest('builds/production/js')))
    .pipe(connect.reload());
  cb();
}

//gulp.task('default', ['html', 'json', 'coffee', 'js', 'compass', 'connect', 'watch']);
exports.default = series(html, json, coffeeMaker, js, compassMaker, imagesReducer, connectServer, watcher);